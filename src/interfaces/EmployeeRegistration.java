    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        try {
            if (jTextField4.getText().isEmpty() || jTNew_invoice_invoice_no2.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Some fields are empty", "Input error", JOptionPane.ERROR_MESSAGE);
            } else {
                int i = JOptionPane.showConfirmDialog(null, "Are you sure you want to add new employee?", "", JOptionPane.YES_NO_OPTION);
                if (i == 0) {
                    JDBC.putdata("INSERT INTO employees VALUES(0,"
                            + "'" + jTNew_invoice_invoice_no1.getText() + "',"
                            + "'" + jTextField4.getText() + "',"
                            + "'" + jTNew_invoice_invoice_no2.getText() + "',"
                            + "'" + jTNew_invoice_invoice_no3.getText() + "',"
                            + "'Active')");
                    employeeId();
                    employeeTable();
                    jTextField4.setText("");
                    jTNew_invoice_invoice_no2.setText("");
                    jTNew_invoice_invoice_no3.setText("");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton7ActionPerformed

