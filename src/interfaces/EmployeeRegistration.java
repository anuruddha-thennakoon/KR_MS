    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        try {
            DefaultTableModel d = (DefaultTableModel) jTable3.getModel();
            String status = d.getValueAt(jTable3.getSelectedRow(), 4).toString();
            String Eid = d.getValueAt(jTable3.getSelectedRow(), 0).toString();
            if (status.equals("Active")) {
                JDBC.putdata("UPDATE employees SET status='Deactive' WHERE e_id='" + Eid + "'");
                employeeTable();
            } else {
                JDBC.putdata("UPDATE employees SET status='Active' WHERE e_id='" + Eid + "'");
                employeeTable();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No item selected", "Input error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton17ActionPerformed

