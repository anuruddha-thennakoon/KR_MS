    void employeeId() {
        try {
            ResultSet rs = JDBC.getdata("select MAX(e_id) FROM employees");
            while (rs.next()) {
                String batch1 = rs.getString("max(e_id)");
                int batch2 = Integer.parseInt(batch1.substring(2));
                int batch3 = (batch2 + 1);
                jTNew_invoice_invoice_no1.setText("EI" + batch3);
            }
        } catch (Exception e) {
            jTNew_invoice_invoice_no1.setText("EI" + 15000001);
        }
    }

    void employeeTable() {
        try {
            DefaultTableModel d = (DefaultTableModel) jTable3.getModel();
            d.setRowCount(0);
            ResultSet rs = JDBC.getdata("SELECT * FROM employees");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("e_id"));
                v.add(rs.getString("f_name"));
                v.add(rs.getString("l_name"));
                v.add(rs.getString("contact"));
                v.add(rs.getString("status"));
                d.addRow(v);
            }
        } catch (Exception e) {
        }
    }

