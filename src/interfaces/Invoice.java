    void invoiceNo() {
        try {
            ResultSet rs = JDBC.getdata("select MAX(invoice_no) FROM invoice_info");
            while (rs.next()) {
                String batch1 = rs.getString("max(invoice_no)");
                int batch2 = Integer.parseInt(batch1.substring(2));
                int batch3 = (batch2 + 1);
                jTNew_invoice_invoice_no1.setText("IN" + batch3);
            }
        } catch (Exception e) {
            jTNew_invoice_invoice_no1.setText("IN" + 15000001);
        }
    }

