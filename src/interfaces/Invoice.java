    void invoiceTable() {
        String iid = null;
        String iname = null;
        try {
            ResultSet rs = JDBC.getdata("SELECT * FROM item WHERE item_combo='" + jCItem_item.getSelectedItem().toString() + "'");
            while (rs.next()) {
                iid = rs.getString("item_id");
                iname = rs.getString("item_name");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        double qty = Double.parseDouble(jTextField3.getText().trim().toString());
        double price = Double.parseDouble(jTextField5.getText().trim().toString());
        DefaultTableModel d = (DefaultTableModel) jTable4.getModel();
        Vector v = new Vector();
        v.add(iid);
        v.add(iname);
        v.add(jCItem_item1.getSelectedItem());
        v.add(jTextField5.getText().trim());
        v.add(jTextField3.getText().trim());
        v.add(df.format(qty * price));
        d.addRow(v);
    }

    public boolean checkDuplicate(int columnIndex, JTable jt, String iid) {
        DefaultTableModel d = (DefaultTableModel) jTable4.getModel();
        int rowCount = d.getRowCount();
        boolean b = false;
        for (int x = 0; x < rowCount; x++) {
            String tiid = jt.getValueAt(x, columnIndex).toString();
            if (iid.equals(tiid)) {
                b = true;
            }
        }
        return b;
    }

    void toatalAmount() {
        try {
            DefaultTableModel d1 = (DefaultTableModel) jTable4.getModel();
            double gross_amount = 0;
            for (int x = 0; x < d1.getRowCount(); x++) {
                gross_amount = gross_amount + Double.parseDouble(jTable4.getValueAt(x, 5).toString());
            }
            DefaultTableModel d2 = (DefaultTableModel) jTable3.getModel();
            for (int x = 0; x < d2.getRowCount(); x++) {
                gross_amount = gross_amount + Double.parseDouble(jTable3.getValueAt(x, 1).toString());
            }
            jTextField9.setText(df.format(gross_amount));
            jTextField13.setText(df.format(gross_amount));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

